#ifndef __PARSE_H__
#define __PARSE_H__

#define ARG_CNT 64
#define PATYPE_ONESPACE 0
#define PATYPE_MANYSPACES 1

extern char *p_arg[ARG_CNT];
extern int p_argc, p_size[ARG_CNT];

/*
	1.	Распознать данные, пришедшие из сети
		и принять соответствующие действия
	2.	---
	3.	Не 0, если ошибка
	4.	---
*/
int parse_out( void );

/*
	1.	Распознать данные, введённые пользователем
		и принять соответствующие действия
	2.	---
	3.	Не 0, если ошибка
	4.	---
*/
int parse_in( void );

/*
	1.	Разбиение сообщения на параметры
	2.	msg - сообщение
	3.	Количество прочитаных параметров,
		0, если ошибка,
		p_arg - список полученых параметров
		p_argc - их количество
	4.	---
*/
int parse( char *msg, int type );

#endif
